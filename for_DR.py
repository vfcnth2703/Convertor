a = [('Живайкина', 300), ('Ильина', 500), ('Беловолова', 300), ('Ершова', 300), ('Комаров', 500), ('Запорожец', 500), ('Беликов', 300), ('Волкова', 300), ('Принцев', 500)]
b = dict(a)
total = 0
print('*' * 22)
for key, value in b.items():
    total += value
    print(f'{key} = {value} руб.')
print('*' * 22)
print(f'Всего сдали {len(b)} человек.')
print(f'Итого: {total} руб.')
